<?php

namespace Diff\LoginBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Declaratie
 */
class Declaratie
{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var integer
     */
    private $tripid;

    /**
     * @var string
     */
    private $code;

    /**
     * @var string
     */
    private $name;

    /**
     * @var \DateTime
     */
    private $date;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set tripid
     *
     * @param integer $tripid
     * @return Declaratie
     */
    public function setTripid($tripid)
    {
        $this->tripid = $tripid;

        return $this;
    }

    /**
     * Get tripid
     *
     * @return integer 
     */
    public function getTripid()
    {
        return $this->tripid;
    }

    /**
     * Set code
     *
     * @param string $code
     * @return Declaratie
     */
    public function setCode($code)
    {
        $this->code = $code;

        return $this;
    }

    /**
     * Get code
     *
     * @return string 
     */
    public function getCode()
    {
        return $this->code;
    }

    /**
     * Set name
     *
     * @param string $name
     * @return Declaratie
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string 
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * Set date
     *
     * @param \DateTime $date
     * @return Declaratie
     */
    public function setDate($date)
    {
        $this->date = $date;

        return $this;
    }

    /**
     * Get date
     *
     * @return \DateTime 
     */
    public function getDate()
    {
        return $this->date;
    }
}
